  function isColliding(cardA, cardB) {
    const personA = getPersonByCard(cardA);
    const personB = getPersonByCard(cardB);
    if (!personA || !personB) return false;

    const ax = personA.x + CARD_SIZE / 2;
    const ay = personA.y + CARD_SIZE / 2;
    const bx = personB.x + CARD_SIZE / 2;
    const by = personB.y + CARD_SIZE / 2;

    return Math.hypot(ax - bx, ay - by) < 200;
  }

  function isGroupFullyAssembled(groupKey) {
    const members = getGroupMembers(groupKey);
    if (members.length < 2) return false;

    const visited = new Set();
    const stack = [members[0]];

    while (stack.length > 0) {
      const currentId = stack.pop();
      if (visited.has(currentId)) continue;
      visited.add(currentId);

      const currentCard = getCardById(currentId);
      if (!currentCard) continue;

      for (const nextId of members) {
        if (nextId === currentId || visited.has(nextId)) continue;
        const nextCard = getCardById(nextId);
        if (!nextCard) continue;

        if (isColliding(currentCard, nextCard)) {
          stack.push(nextId);
        }
      }
    }

    return visited.size === members.length;
  }

